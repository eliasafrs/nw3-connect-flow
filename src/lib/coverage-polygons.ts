// Ray-casting point-in-polygon algorithm
function isPointInPolygon(point: Coordinate, polygon: Coordinate[]): boolean {
  const [y, x] = point;
  let inside = false;

  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    const [yi, xi] = polygon[i];
    const [yj, xj] = polygon[j];

    const intersect =
      yi > y !== yj > y && x < ((xj - xi) * (y - yi)) / (yj - yi) + xi;

    if (intersect) inside = !inside;
  }

  return inside;
}

export function isInCoverageArea(lat: number, lng: number): boolean {
  const point: Coordinate = [lat, lng];
  return coveragePolygons.some((poly) => isPointInPolygon(point, poly.coords));
}

export async function geocodeAddress(
  logradouro: string,
  bairro: string,
  localidade: string,
  uf: string,
  cep: string
): Promise<{ lat: number; lng: number } | null> {
  const queries = [
    // Try full address first
    logradouro
      ? `${logradouro}, ${bairro}, ${localidade}, ${uf}, Brazil`
      : `${bairro}, ${localidade}, ${uf}, Brazil`,
    // Fallback: neighborhood + city
    `${bairro}, ${localidade}, ${uf}, Brazil`,
    // Fallback: formatted CEP
    `${cep.slice(0, 5)}-${cep.slice(5)}, São Paulo, Brazil`,
  ];

  for (const q of queries) {
    try {
      const response = await fetch(
        `https://nominatim.openstreetmap.org/search?q=${encodeURIComponent(q)}&format=json&limit=1&countrycodes=br`,
        { headers: { "User-Agent": "NW3-Internet-App" } }
      );
      const data = await response.json();
      if (data && data.length > 0) {
        return { lat: parseFloat(data[0].lat), lng: parseFloat(data[0].lon) };
      }
    } catch {
      continue;
    }
  }
  return null;
}
